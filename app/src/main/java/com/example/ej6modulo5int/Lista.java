package com.example.ej6modulo5int;

public class Lista {
    private String name;
    private String url;
    private String info;

    public Lista(String name, String url, String info) {
        this.name = name;
        this.url = url;
        this.info = info;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }

    public String getInfo() {
        return info;
    }

}
